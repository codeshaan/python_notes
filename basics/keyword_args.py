# Keyword arguments

def printDetails (name, email, password, DOB):
    print ("Name = ", name)
    print ("Email = ", email)
    print ("Password = ", password)
    print ("DOB = ", DOB)

printDetails(email = 'shaanalam369@gmail.com', name = 'Shaan Alam', DOB = '15/10/2002', password = '**********')

def printDetails2(name, email, password, DOB):
    print ('Name = ', name)
